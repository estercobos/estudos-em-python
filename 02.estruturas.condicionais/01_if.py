"""
O 'if' é usado para checar se uma condição é verdadeira.
Se for verdadeira, ele executa o que está dentro dele. Se não for, ele ignora e segue em frente.
"""

# Pergunta a idade da pessoa
idade = int(input("Digite sua idade: "))

# Se a idade for maior ou igual a 18, a pessoa é maior de idade
if idade >= 18:
    print("Você é maior de idade!")  # Se a condição for verdadeira, executa isso.
