# O laço 'while' repete um bloco de código enquanto a condição for verdadeira. Nesse exemplo, o while irá contar de 1 até 5.

contador = 1  # Iniciamos o contador com o valor 1

while contador <= 5:  # A condição é que o contador seja menor ou igual a 5
    print(contador)  # Imprimimos o valor de 'contador'
    contador += 1  # A cada iteração, incrementamos 1 no contador